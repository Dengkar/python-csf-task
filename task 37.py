month = input()
if month == 'January' or month == 'February' or month == 'March' or month == 'March' or month == 'April' or month == 'May':
    print("spring")
if month == 'june' or month == 'july' or month == 'August':
    print("summer")
if month == 'september' or month == 'october' or month == 'November':
    print('fall')
if month == 'December':
    print('winter')
else:
    print('invalid month')
